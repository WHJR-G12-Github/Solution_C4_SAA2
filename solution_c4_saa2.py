# Defining a function 'square()' with 'side' as parameter
def square(side):
    # Calculate 'area' 
    area = side*side
    # Calculate 'volume'
    volume = side*side*side
    # Printing the results
    print("Area of the square:",area)
    print("Volume of the square:",volume)

  
# Calling the function 'square()' with '8' as argument
square(8)
